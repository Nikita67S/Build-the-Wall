def volume(*, length_wall: int, thickness_wall: int, height_wall: int) -> float:
    """

    Args:
        length_wall:
        thickness_wall:
        height_wall:

    Returns:#Conversions to meters / Obtaining volume

    """
    return (length_wall / 1000) * (thickness_wall / 1000) * (height_wall / 1000)






